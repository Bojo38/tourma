    public jdgRevisions(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();

        GraphicsEnvironment ge = GraphicsEnvironment.getLocalGraphicsEnvironment();
        GraphicsDevice gs = ge.getDefaultScreenDevice();
        DisplayMode dmode = gs.getDisplayMode();

        this.setSize(580, 350);

        if (dmode != null) {
            int screenWidth = dmode.getWidth();
            int screenHeight = dmode.getHeight();
            this.setLocation((screenWidth - this.getWidth()) / 2, (screenHeight - this.getHeight()) / 2);
        }

        Vector versions=new Vector();
        Vector descriptions=new Vector();

        int n=1;
        String tmpV=Version.getSingleton().getProperty(Integer.toString(n)+"-V");
        String tmpD=Version.getSingleton().getProperty(Integer.toString(n)+"-D");

        while (tmpV!=null)
        {
            versions.add(tmpV);
            descriptions.add(tmpD);
            n++;
            tmpV=Version.getSingleton().getProperty(Integer.toString(n)+"-V");
            tmpD=Version.getSingleton().getProperty(Integer.toString(n)+"-D");
        }

    mtRevisions model=new mtRevisions(versions, descriptions);
    jxtVersions.setModel(model);
    jxtVersions.setDefaultRenderer(String.class, model);
    jxtVersions.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

    pack();
    repaint();

    }


    public void update() {
        Vector<Coach> vector = new Vector<Coach>();

        for (int i = 0; i < _tournament.getCoachs().size(); i++) {
            Coach c = _tournament.getCoachs().get(i);
            for (int j = 0; j < _group._rosters.size(); j++) {
                if (_group._rosters.get(j)._name.equals(c._roster._name)) {
                    vector.add(c);
                    break;
                }
            }
        }

        mjtRankingIndiv tableModel = new mjtRankingIndiv(_roundNumber,
                _tournament.getParams()._ranking1,
                _tournament.getParams()._ranking2,
                _tournament.getParams()._ranking3,
                _tournament.getParams()._ranking4,
                _tournament.getParams()._ranking5,
                vector, _tournament.getParams()._teamTournament);
        jtbGroup.setModel(tableModel);
        jtbGroup.setDefaultRenderer(String.class, tableModel);
        jtbGroup.setDefaultRenderer(Integer.class, tableModel);

        setColumnSize(jtbGroup);
    }

    public void setColumnSize(JTable t) {
        FontMetrics fm = t.getFontMetrics(t.getFont());
        for (int i = 0; i < t.getColumnCount(); i++) {
            int max = 0;
            for (int j = 0; j < t.getRowCount(); j++) {
                Object value = t.getValueAt(j, i);
                String tmp = "";
                if (value instanceof String) {
                    tmp = (String) value;
                }
                if (value instanceof Integer) {
                    tmp = "" + (Integer) value;
                }
                int taille = fm.stringWidth(tmp);
                if (taille > max) {
                    max = taille;
                }
            }
            String nom = (String) t.getColumnModel().getColumn(i).getIdentifier();
            int taille = fm.stringWidth(nom);
            if (taille > max) {
                max = taille;
            }
            t.getColumnModel().getColumn(i).setPreferredWidth(max + 10);
        }
    }


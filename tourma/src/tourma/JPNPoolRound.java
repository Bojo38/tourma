    public JPNPoolRound(Round r, Tournament t, Pool p) {
        _p = p;
        initComponents();
        _round = r;
        _tournament = t;

        for (int i = 0; i < _tournament.getParams()._criterias.size(); i++) {
            Criteria criteria = _tournament.getParams()._criterias.get(i);
            if (!_tournament.getParams()._teamTournament) {
                JPNAnnexRanking jpn = new JPNAnnexRanking(criteria._name, criteria, _tournament, _round, false, false, p._coachs, new Vector<Team>());
                jtpAnnexRank.add(criteria._name, jpn);
            } else {

                Vector<Coach> v = new Vector<Coach>();
                for (int j = 0; j < p._teams.size(); j++) {
                    for (int k = 0; k < p._teams.get(j)._coachs.size(); k++) {
                        v.add(p._teams.get(j)._coachs.get(k));
                    }
                }
                JPNAnnexRanking jpn = new JPNAnnexRanking(criteria._name, criteria, _tournament, _round, false, true, v, p._teams);
                jtpAnnexRank.add(criteria._name, jpn);
                jpn = new JPNAnnexRanking(criteria._name, criteria, _tournament, _round, false, false, v, p._teams);
                jtpAnnexRank.add(criteria._name, jpn);
            }
        }
        update();
    }


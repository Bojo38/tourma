    private void jbtGroupToRightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtGroupToRightActionPerformed

        if (jlsLeft.getSelectedIndex() > -1) {
            final int index = jlsLeft.getSelectedIndex();
            final RosterType roster = mTournament.getGroups().get(jcbGroupLeft.getSelectedIndex()).mRosters.get(index);
            mTournament.getGroups().get(jcbGroupLeft.getSelectedIndex()).mRosters.remove(roster);
            mTournament.getGroups().get(jcbGroupRight.getSelectedIndex()).mRosters.add(roster);

            ArrayList<RosterType> rosters = mTournament.getGroups().get(jcbGroupLeft.getSelectedIndex()).mRosters;
            final DefaultListModel listModelLeft = new DefaultListModel();
            for (int i = 0; i < rosters.size(); i++) {
                listModelLeft.addElement(rosters.get(i).mName);
            }
            jlsLeft.setModel(listModelLeft);

            final DefaultListModel listModelRight = new DefaultListModel();
            rosters = mTournament.getGroups().get(jcbGroupRight.getSelectedIndex()).mRosters;
            for (int i = 0; i < rosters.size(); i++) {
                listModelRight.addElement(rosters.get(i).mName);
            }
            jlsRight.setModel(listModelRight);
        }
    }//GEN-LAST:event_jbtGroupToRightActionPerformed

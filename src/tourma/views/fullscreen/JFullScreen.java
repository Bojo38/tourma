    protected GridBagConstraints getGridbBagConstraints(int x, int y, int height, int width) {
        GridBagConstraints gbc = new GridBagConstraints();
        gbc.gridx = x;
        gbc.gridy = y;
        gbc.fill = GridBagConstraints.BOTH;
        gbc.weightx = 1.0;
        gbc.weighty = 1.0;
        gbc.gridheight = height;
        gbc.gridwidth = width;
        return gbc;
    }

